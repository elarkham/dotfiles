#include "frog.h"
#include <iostream>
#include <ctime>
#include <cstdlib>

using namespace std;

int main(){
	Frog frogger;
	int steps,index;
	double dist, maxDistance=0.0;
	srand(time(NULL));	

	cout<<"How many steps in your simulation? ";cin>>steps;
	
	for (index =1; index<=steps;index++){
		frogger.jump();
		dist=frogger.distance();
		if(dist>maxDistance){maxDistance=dist;}
	}
	frogger.display();
	cout<<"You wandered "<<maxDistance<<" away from home"<<endl;
}
